class HomeAppProducts extends StatefulWidget {
  String txt;
   HomeAppProducts(this.txt);
  @override
  State<HomeAppProducts> createState() => HomeAppProductsState(txt);
}
 


class HomeAppProductsState extends State<HomeAppProducts> {
    String txt;
   HomeAppProductsState(this.txt);
  @override
  Widget build(BuildContext context) {
    return Column(
      children: [
        Padding(
          padding:
              EdgeInsets.symmetric(horizontal: getProportionateScreenWidth(20)),
          child: SectionTitle(title: txt, press: () {}),
        ),
        SizedBox(height: getProportionateScreenWidth(20)),
        SingleChildScrollView(
          scrollDirection: Axis.horizontal,
          child: Row(
            children: [
              ...List.generate(
                demoHomeapp.length,
                (index) {
                  if (demoHomeapp[index].isPopular) {
                    return HomeAppCard(product: demoHomeapp[index]);
                  }

                  return const SizedBox
                      .shrink(); // here by default width and height is 0
                },
              ),
              SizedBox(width: getProportionateScreenWidth(20)),
            ],
          ),
        )
      ],
    );
  }
}





